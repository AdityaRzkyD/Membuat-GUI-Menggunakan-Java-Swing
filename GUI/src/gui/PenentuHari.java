    private void HitungJumlahHhariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungJumlahHhariActionPerformed
        // TODO add your handling code here:
        HitungHari h = new HitungHari();
        h.tahun = Integer.parseInt(Tahun.getText());
        if(Bulan.getSelectedItem().equals("Februari")){
            h.hitung();
        }
        else if (Bulan.getSelectedItem().equals("April") ||
                Bulan.getSelectedItem().equals("Juni") ||
                Bulan.getSelectedItem().equals("September") ||
                Bulan.getSelectedItem().equals("November")){
            h.hari = 30;
        }
        else{
            h.hari = 31;
        }
        JumlahHari.setText("Jumlah hari pada bulan "+Bulan.getSelectedItem()+" Tahun "+ h.tahun +" adalah "+h.hari);
    }//GEN-LAST:event_HitungJumlahHhariActionPerformed

