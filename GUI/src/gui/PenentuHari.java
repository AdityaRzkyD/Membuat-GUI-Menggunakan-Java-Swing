    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        HitungHari h = new HitungHari();
        h.tahun = Integer.parseInt(Tahun.getText());
        if(Bulan.getSelectedItem().equals("Februari")){
            h.hitung();
        }
        else if (Bulan.getSelectedItem().equals("April") ||
                Bulan.getSelectedItem().equals("Juni") ||
                Bulan.getSelectedItem().equals("September") ||
                Bulan.getSelectedItem().equals("November")){
            h.hari = 30;
        }
        else{
            h.hari = 31;
        }
        try{
            BufferedWriter out = new BufferedWriter(new FileWriter("Simpan.txt"));
            String Tampilkan = "Tahun : "+h.tahun+"\n"+"Bulan : "+Bulan.getSelectedItem()+"\n"+"Jumlah Hari : "+h.hari;
            out.write(Tampilkan);
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan Dalam File");
            out.close();
        }catch(Exception e){
            System.err.println("Error : "+e.getMessage());
        }
    }//GEN-LAST:event_SimpanActionPerformed

